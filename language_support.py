SUPPORTED_LANGUAGES = {code: data["lang_name"] for code, data in UI_STRINGS.items()}
DEFAULT_LANGUAGE = "en"


def get_ui_strings(lang_code: str) -> dict[str, str]:
    """Return UI strings for the given language code, falling back to English."""
    return UI_STRINGS.get(lang_code, UI_STRINGS[DEFAULT_LANGUAGE])


def detect_language(text: str) -> str:
    """
    Detect the language of the provided text.
    Returns a language code (e.g. 'hi', 'ta') or 'en' as default.
    """
    try:
        from langdetect import detect, LangDetectException  # type: ignore
        detected = detect(text)
        # Map langdetect codes to our supported codes
        mapping = {
            "hi": "hi", "bn": "bn", "ta": "ta", "te": "te",
            "mr": "mr", "kn": "kn", "ml": "ml", "pa": "pa",
            "gu": "gu", "en": "en",
        }
        return mapping.get(detected, "en")
    except Exception:
        return "en"


def translate_text(text: str, target_lang: str) -> str:
    """
    Translate text to the target language using deep-translator.
    Falls back to the original text if translation fails.
    """
    if target_lang == "en":
        return text
    try:
        from deep_translator import GoogleTranslator  # type: ignore
        # deep-translator uses full language names or ISO codes
        lang_map = {
            "hi": "hindi", "bn": "bengali", "ta": "tamil",
            "te": "telugu", "mr": "marathi", "kn": "kannada",
            "ml": "malayalam", "pa": "punjabi", "gu": "gujarati",
        }
        target = lang_map.get(target_lang, "english")
        translated = GoogleTranslator(source="auto", target=target).translate(text)
        return translated or text
    except Exception:
        return text
